try:
    ph = float(input("Введите значение pH (от 0 до 14): "))

    # 2. Проверяем условия с помощью полной конструкции if/elif/else
    if ph < 7:
        print("Кислая среда")
        
    elif ph == 7:
        # Теперь это условие снова на месте, и программа не будет молчать
        print("Нейтральная среда")
        
    else:
        # Сюда попадут все значения больше 7
        print("Щелочная среда")

except ValueError:
    print("Ошибка! Пожалуйста, введите числовое значение.")
